#include "../Headers/cube3d.h"

void	free_str_1(char **str, int o)
{
	if (str)
	{
		while (o >= 0)
		{
			free(str[o]);
			str[o] = 0;
			o--;
		}
		free(str);
		str = 0;
	}
}
